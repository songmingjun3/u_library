public class BookCollectionInfo {
    private String BookName;
    private String Link;

    public void setBookName(String bookName) {
        BookName = bookName;
    }

    public void setLink(String link) {
        Link = link;
    }

    public String getLink() {
        return Link;
    }

    public String getBookName() {
        return BookName;
    }
}
